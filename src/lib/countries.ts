const CODE_SET: ReadonlySet<string> = new Set(COUNTRY_CODES);

/** True when `code` is a known ISO 3166-1 alpha-2 country code. */
export function isValidCountryCode(code: string): boolean {
  return CODE_SET.has(code);
}

/**
 * Localized country name for an ISO code, or the raw code as a fallback.
 * Returns "" for an empty code so callers can render a placeholder.
 */
export function getCountryName(code: string, locale: string): string {
  if (!code) return "";
  try {
    return new Intl.DisplayNames([locale], { type: "region" }).of(code) ?? code;
  } catch {
    return code;
  }
}

export type CountryOption = { code: string; name: string };

/**
 * All countries as `{ code, name }`, names localized to `locale` and sorted
 * by localized name. Codes that don't resolve to a name are dropped.
 */
export function getCountryOptions(locale: string): CountryOption[] {
  const display = new Intl.DisplayNames([locale], { type: "region" });
  return COUNTRY_CODES.map((code) => ({
    code,
    name: display.of(code) ?? code,
  }))
    .filter((option) => option.name !== option.code)
    .sort((a, b) => a.name.localeCompare(b.name, locale));
}

